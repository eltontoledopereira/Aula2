using System;
using System.Collections.Generic;
using System.Text;

namespace MeusExercícios
{
    public class Cálculos
    {
        public static decimal Divisao(decimal num1, decimal num2)
        {
            return num1 / num2;
        }

        public static decimal Multiplicacao(decimal num1, decimal num2)
        {
            return num1 * num2;
        }

        public static decimal Subtracao(decimal num1, decimal num2)
        {
            return num1 - num2;
        }

        public static decimal Soma(decimal num1, decimal num2)
        {
            return num1 + num2;
        }
    }
}
